#include <unistd.h>

int	main(int argc, char *argv[])
{
	char	*ptr;

	ptr = argv[0];
	while (*ptr)
	{
		write(1, ptr, 1);
		ptr++;
	}
	write(1, "\n", 1);
	return (0);
}
