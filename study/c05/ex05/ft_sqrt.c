int	ft_sqrt(int nb)
{
	long	i;

	if (nb <= 0)
		return (0);
	i = 1;
	while (i * i <= (long) nb)
	{
		if (i * i == (long) nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
