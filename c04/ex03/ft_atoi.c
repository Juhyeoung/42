int	ft_atoi(char *str)
{
	int		i;
	long	result;
	int		m;

	i = 0;
	result = 0;
	m = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			m *= -1;
		i++;
	}	
	while ((str[i] >= '0' && str[i] <= '9'))
	{
		result *= 10;
		result += (str[i] - '0');
		i++;
	}
	return (result * m);
}
